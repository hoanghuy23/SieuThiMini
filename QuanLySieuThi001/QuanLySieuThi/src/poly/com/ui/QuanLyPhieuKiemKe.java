    private void printBill(int id,String nameReport){
        try {
        Hashtable map = new Hashtable();
            //JasperReport
            JasperReport jr = JasperCompileManager.compileReport("src/poly/com/ui/"+nameReport);
            map.put("MaPN",(id));
            //JasperPrint
            @SuppressWarnings("unchecked")
            JasperPrint jp = JasperFillManager.fillReport(jr, map, XJdbc.conn);
            //JasperView
            JasperViewer.viewReport(jp,false);
            //JasperExport
            JasperExportManager.exportReportToPdfFile(jp, "PhieuKiemKe.pdf");
        } catch (JRException ex) {

            ex.printStackTrace();
        }
    }
    
