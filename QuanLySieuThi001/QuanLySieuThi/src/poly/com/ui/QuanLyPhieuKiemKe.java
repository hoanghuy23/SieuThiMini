    private void fillTablePhieuKK(){
        NhanVienDAO nvDAO = new NhanVienDAO();
        kkModel = (DefaultTableModel) tbl_listPhieuKK.getModel();
        kkModel.setRowCount(0);
        dskk = kkDAO.selectAll();
        try {
            for(PhieuKiemKe pkk : dskk){
                Object[] rows = {pkk.getMaPhieuKK(),pkk.getMaNV(),nvDAO.selectById(pkk.getMaNV()).getHoTen(),pkk.getThoiDiemLap()};
                kkModel.addRow(rows);
                
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu");
            e.printStackTrace();
        }
    }

