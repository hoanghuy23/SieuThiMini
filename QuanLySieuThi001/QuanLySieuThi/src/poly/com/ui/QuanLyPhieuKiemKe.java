    private void fillTableKhoHang() {
        SanPhamDAO spDAO = new SanPhamDAO();
        ThongKeDAO tkDAO = new ThongKeDAO();
        DefaultTableModel khModel = (DefaultTableModel) tblKhoHang.getModel();
        khModel.setRowCount(0);
        List<Object[]> list = tkDAO.getKhoHang();
        for(Object[] row : list){
            //Lấy mã loại hàng
            int maLH = spDAO.selectById((int)row[0]).getMaLH();
            //TênSP,HSD
            String tenSP = spDAO.selectById((int)row[0]).getTenSP();
            String HSD = spDAO.selectById((int)row[0]).getHsd();
            //ADD VÀO ROW
            khModel.addRow(new Object[]{maLH,row[0],tenSP,row[1],HSD,row[2]});  
        }
    }
    
