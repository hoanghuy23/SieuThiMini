    private void btnLayLaiMKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLayLaiMKActionPerformed
        String mkMoi = new String(txtMatKhau.getPassword()) ;
        String xnMK = new String(txtMatKhau2.getPassword());
        String tk = new String(txtTaiKhoan.getText());
        if(mkMoi.equals(xnMK)){
            if(txtXacNhan.getText().equals(code)){
                try {
                    nvDAO.updatePassword(txtTaiKhoan.getText(),new String(txtMatKhau.getPassword()));
                    MsgBox.alert(this, "Lấy lại mật khẩu thành công");
                    GenerateQRCode QR = new GenerateQRCode();
//                    QR.generateQRCode(tk, "Barcode\\");
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }else{
                MsgBox.alert(this, "Mã xác nhận không đúng");
                lblxn.setVisible(true);
                txtMatKhau.setFocusable(true);
                
            }
        }else{
            MsgBox.alert(this, "Nhập lại mật khẩu không đúng");
            lblMK1.setVisible(true);
            lblmk2.setVisible(true);
            txtXacNhan.setFocusable(true);
            
        }
        
        
    }//GEN-LAST:event_btnLayLaiMKActionPerformed

