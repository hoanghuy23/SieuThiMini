    private void txtEmailKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtEmailKeyReleased
        NhanVien nv = nvDAO.selectByEmail(txtEmail.getText());
        if(nv != null){
            lblCheck.setVisible(true);
            txtTaiKhoan.setText(nv.getTaiKhoan());
            txtXacNhan.setEditable(true);
            txtMatKhau.setEditable(true);
            txtMatKhau2.setEditable(true);
            // Random code 
            Random rd = new Random();
            code= String.valueOf(rd.nextInt(20000));
            System.out.println(code);
            SendEmailToRecover.CodeToRecover(txtEmail.getText(), code+"");
            lblThongBao.setVisible(true);
        //
        }
    }//GEN-LAST:event_txtEmailKeyReleased
    
