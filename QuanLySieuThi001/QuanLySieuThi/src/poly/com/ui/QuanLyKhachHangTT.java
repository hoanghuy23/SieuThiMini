    KhachHangDAO dao = new KhachHangDAO();
    int row = -1;
    
    private void init(){
        setLocationRelativeTo(null) ;
        
        this.fillTable();
        this.updateStatus();
    }
    
    private void fillTable(){
        DefaultTableModel model = (DefaultTableModel) tblKhachHang.getModel();
        model.setRowCount(0);
        try {
            List<KhacHangTT> list = dao.selectAll();
            for(KhacHangTT kh : list){
                Object[] row = {
                    kh.getMaKH(),
                    kh.getHoTen(),
                    kh.getDiaChi(),
                    kh.getNgayCapThe(),
                    kh.getNgayMuaGanNhat(),
                    kh.getDiemThuong(),
                    kh.getImage()
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu !!");
            e.printStackTrace();
            
        }
    }
    
    private void updateStatus(){
        boolean edit = (this.row >= 0);
        boolean first = (this.row == 0);
        boolean last = (this.row == tblKhachHang.getRowCount()-1);
        
        txtMaKH.setEditable(!edit);
        btn_DK.setEnabled(!edit);
        btn_sua.setEnabled(edit);
        btn_xoa.setEnabled(edit);
        
        btnFirst.setEnabled(edit && !first);
        btnPrev.setEnabled(edit && !first);
        btnNext.setEnabled(edit && !last);
        btnLast.setEnabled(edit && !last);
    }
    
    private void setForm(KhacHangTT kh){
        txtMaKH.setText(kh.getMaKH()+"");
        txtTenKH.setText(kh.getHoTen());
        txtDiaChi.setText(kh.getDiaChi());
        jDateNL.setDate(kh.getNgayCapThe());
        jDateNL1.setDate(kh.getNgayMuaGanNhat());
        txtDiemThuong.setText(kh.getDiemThuong()+"");
        if(kh.getImage() == null){
            
        }else{
            image.setIcon(XImage.read(kh.getImage()));
            image.setToolTipText(kh.getImage());
        }
        
        
    }
    
    KhacHangTT getForm(){
        KhacHangTT kh = new KhacHangTT();
        kh.setMaKH(Integer.parseInt(txtMaKH.getText()));
        kh.setHoTen(txtTenKH.getText());
        kh.setDiaChi(txtDiaChi.getText());
        kh.setNgayCapThe(jDateNL.getDate());
        kh.setNgayMuaGanNhat(jDateNL1.getDate());
        kh.setDiemThuong(Double.parseDouble(txtDiemThuong.getText()));
        kh.setImage(image.getToolTipText());
        return kh;
        
    }
    
    void clearForm(){
        KhacHangTT kh = new KhacHangTT();
        kh.setMaKH(list.size()+4);
        this.setForm(kh);
        this.row = -1;
        this.updateStatus();
    }
    
    void edit(){
        int maKH = (int) tblKhachHang.getValueAt(this.row, 0);
        KhacHangTT kh = dao.selectById(maKH);
        this.setForm(kh);
        this.updateStatus();
    }
    
    void first(){
        this.row = 0;
        this.edit();
        tblKhachHang.setSelectionBackground(Color.RED);
        tblKhachHang.changeSelection(row, WIDTH, false, false);
        
    }
    void prev(){
        if(this.row > 0){
            this.row--;
            this.edit();
            tblKhachHang.setSelectionBackground(Color.RED);
            tblKhachHang.changeSelection(row, WIDTH, false, false);
        }
    }
    void next(){
        if(this.row < tblKhachHang.getRowCount() - 1){
            this.row++;
            this.edit();
            tblKhachHang.setSelectionBackground(Color.RED);
            tblKhachHang.changeSelection(row, WIDTH, false, false);
        }
    }
    void last(){
        this.row = tblKhachHang.getRowCount() - 1;
        this.edit();
        tblKhachHang.setSelectionBackground(Color.RED);
        tblKhachHang.changeSelection(row, WIDTH, false, false);
    }
