    void insert(){
        if(Auth.isManager()){
            KhacHangTT model = getForm();
            model.setNgayCapThe(XDate.convertDate(model.getNgayCapThe()));
            model.setNgayMuaGanNhat(XDate.convertDate(model.getNgayMuaGanNhat()));
            try {
                dao.insert(model);
                this.fillTable();
                this.clearForm();
                MsgBox.alert(this, "Thêm mới thành công!!");
            } catch (Exception e) {
                e.printStackTrace();
                MsgBox.alert(this, "Thêm mới thất bại !!");
            }
        }else{
            MsgBox.alert(this, "Bạn không có quyền thêm mới khách hàng");
        }
        
    }
    
    void update(){
        if(Auth.isManager()){
            KhacHangTT model = getForm();
            model.setNgayCapThe(XDate.convertDate(model.getNgayCapThe()));
            model.setNgayMuaGanNhat(XDate.convertDate(model.getNgayMuaGanNhat()));
            try {
                dao.update(model);
                this.fillTable();
                MsgBox.alert(this, "Cập nhật thành công !!");
            } catch (Exception e) {
                e.printStackTrace();
                MsgBox.alert(this, "Cập nhật thất bại !!");
            }
        }else{
            MsgBox.alert(this, "Bạn không có quyền cập nhật ");
        }
        
    }
    
    void delete(){
        if(!Auth.isManager()){
            MsgBox.alert(this, "Bạn không được phân quyền để xóa khách hàng này.");
        }else if(MsgBox.confirm(this, "Bạn thực sự muốn xóa khách hàng này !!")){
            int maKH = Integer.valueOf(txtMaKH.getText());
            try {
                dao.delete(maKH);
                this.fillTable();
                this.clearForm();
                MsgBox.alert(this, "Xóa khách hàng thành công.");
            } catch (Exception e) {
                e.printStackTrace();
                MsgBox.alert(this, "Xóa khách hàng thất bại!1");
            }
        }
    }
    
    
